package main

import (
	"bufio"
	"fmt"
	"strings"
)

const (
	east  = '>'
	south = 'v'
	empty = '.'
)

type state [][]byte

func (s state) move() int {
	x := len(s)
	y := len(s[0])
	var n int
	// east
	for i := 0; i < x; i++ {
		zeroOccupied := s[i][0] != empty
		for j := 0; j < y; j++ {
			if s[i][j] != east {
				continue
			}
			next := j + 1
			if next >= y {
				if zeroOccupied {
					continue
				}
				next -= y
			}
			if s[i][next] != empty {
				continue
			}
			s[i][j] = empty
			s[i][next] = east
			n++
			j++
		}
	}
	// south
	for j := 0; j < y; j++ {
		zeroOccupied := s[0][j] != empty
		for i := 0; i < x; i++ {
			if s[i][j] != south {
				continue
			}
			next := i + 1
			if next >= x {
				if zeroOccupied {
					continue
				}
				next -= x
			}
			if s[next][j] != empty {
				continue
			}
			s[i][j] = empty
			s[next][j] = south
			n++
			i++
		}
	}
	return n
}

func (s state) String() string {
	lines := make([]string, 0, len(s))
	for i := range s {
		lines = append(lines, string(s[i]))
	}
	return strings.Join(lines, "\n")
}

func readMap(input string) state {
	var m state
	scanner := bufio.NewScanner(strings.NewReader(input))
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line == "" {
			continue
		}
		m = append(m, []byte(line))
	}
	return m
}

func main() {
	m := readMap(input)
	for i := 1; ; i++ {
		if m.move() == 0 {
			fmt.Println(i)
			break
		}
	}
}

